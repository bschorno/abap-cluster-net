using ABAPNet.Cluster.Attributes;

namespace ABAPNet.Cluster.Test.Unit
{
    public class Write_DatsTims
    {
        [Fact]
        public void Write_Dats_And_Tims()
        {
            DataBuffer dataBuffer = new DataBuffer();

            byte[] bufferExpected = {
                0xFF, 0x06, 0x02, 0x01, 0x01, 0x02, 0x80, 0x00, 0x34, 0x31, 0x30, 0x33,
                0x00, 0x00, 0x00, 0x00, 0x02, 0x0E, 0x00, 0x00, 0x00, 0x00, 0x1C, 0x00,
                0x00, 0x00, 0x66, 0x04, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
                0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
                0x44, 0x00, 0x41, 0x00, 0x54, 0x00, 0x41, 0x00, 0xAB, 0x0E, 0x00, 0x00,
                0x00, 0x00, 0x1C, 0xAA, 0x01, 0x00, 0x00, 0x00, 0x00, 0x10, 0xAA, 0x03,
                0x00, 0x00, 0x00, 0x00, 0x0C, 0xAC, 0x0E, 0x00, 0x00, 0x00, 0x00, 0x1C,
                0xBC, 0x00, 0x00, 0x00, 0x1C, 0x32, 0x00, 0x30, 0x00, 0x32, 0x00, 0x33,
                0x00, 0x30, 0x00, 0x32, 0x00, 0x31, 0x00, 0x37, 0x00, 0x31, 0x00, 0x32,
                0x00, 0x30, 0x00, 0x30, 0x00, 0x30, 0x00, 0x30, 0x00, 0xBD, 0x04
            };

            var bufferActual = dataBuffer.Export(new Cluster()
            {
                Field = new DateTimeStruct
                {
                    Date = new DateOnly(2023, 02, 17),
                    Time = new TimeOnly(12, 00, 00)
                }
            });

            Assert.Equal(bufferExpected, bufferActual);
        }

        private class Cluster
        {
            [ClusterFieldName("DATA")]
            [FlatStruct]
            public DateTimeStruct Field { get; set; }
        }

        private struct DateTimeStruct
        {
            [Dats]
            public DateOnly Date { get; set; }

            [Tims]
            public TimeOnly Time { get; set; }
        }
    }
}
